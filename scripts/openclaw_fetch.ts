// -------------------- XML parsing helpers --------------------

function stripHtml(html: string): string {
  return html
    .replace(/<[^>]*>/g, '')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&nbsp;/g, ' ')
    .replace(/&#(\d+);/g, (_, code) => String.fromCharCode(parseInt(code)))
    .trim();
}

function extractCDATA(text: string): string {
  const cdataMatch = text.match(/<!\[CDATA\[([\s\S]*?)\]\]>/);
  return cdataMatch ? cdataMatch[1] : text;
}

function getTagContent(xml: string, tagName: string): string {
  const patterns = [
    new RegExp(`<${tagName}[^>]*>([\\s\\S]*?)</${tagName}>`, 'i'),
    new RegExp(`<${tagName}[^>]*/>`, 'i'),
  ];

  for (const pattern of patterns) {
    const match = xml.match(pattern);
    if (match?.[1]) return extractCDATA(match[1]).trim();
  }
  return '';
}

function getAttrValue(xml: string, tagPattern: string, attrName: string): string {
  const pattern = new RegExp(`<${tagPattern}[^>]*\\s${attrName}=["']([^"']*)["'][^>]*/?>`, 'i');
  const match = xml.match(pattern);
  return match?.[1] || '';
}

function parseDate(dateStr: string): Date | null {
  if (!dateStr) return null;
  const d = new Date(dateStr);
  if (!isNaN(d.getTime())) return d;
  return null;
}

function parseRSSItems(xml: string): Array<{ title: string; link: string; pubDate: string; description: string }> {
  const items: Array<{ title: string; link: string; pubDate: string; description: string }> = [];

  const isAtom = (xml.includes('<feed') && xml.includes('xmlns="http://www.w3.org/2005/Atom"')) || xml.includes('<feed ');

  if (isAtom) {
    const entryPattern = /<entry[\s>]([\s\S]*?)<\/entry>/gi;
    let entryMatch;
    while ((entryMatch = entryPattern.exec(xml)) !== null) {
      const entryXml = entryMatch[1];
      const title = stripHtml(getTagContent(entryXml, 'title'));

      // Atom link: prefer rel="alternate"
      let link = getAttrValue(entryXml, 'link[^>]*rel="alternate"', 'href');
      if (!link) link = getAttrValue(entryXml, 'link', 'href');

      const pubDate = getTagContent(entryXml, 'published') || getTagContent(entryXml, 'updated');

      const description = stripHtml(
        getTagContent(entryXml, 'summary') ||
          getTagContent(entryXml, 'content') ||
          ''
      );

      if (title && link) items.push({ title, link, pubDate, description });
    }
  } else {
    const itemPattern = /<item[\s>]([\s\S]*?)<\/item>/gi;
    let itemMatch;
    while ((itemMatch = itemPattern.exec(xml)) !== null) {
      const itemXml = itemMatch[1];
      const title = stripHtml(getTagContent(itemXml, 'title'));
      const link = stripHtml(getTagContent(itemXml, 'link')) || getAttrValue(itemXml, 'link', 'href');
      const pubDate = getTagContent(itemXml, 'pubDate') || getTagContent(itemXml, 'dc:date');
      const description = stripHtml(
        getTagContent(itemXml, 'description') ||
          getTagContent(itemXml, 'content:encoded') ||
          ''
      );

      if (title && link) items.push({ title, link, pubDate, description });
    }
  }

  return items;
}

// -------------------- fetch + concurrency --------------------

type OutArticle = {
  title: string;
  link: string;
  publishedAt: string; // ISO
  description: string;
  sourceName: string;
  sourceUrl: string;
};

type OutJson = {
  generatedAt: string;
  hours: number;
  totalFeeds: number;
  okFeeds: number;
  failedFeeds: Array<{ name: string; xmlUrl: string; error: string }>;
  totalArticles: number;
  articles: OutArticle[];
};

async function fetchWithTimeout(url: string, timeoutMs: number): Promise<string> {
  const controller = new AbortController();
  const id = setTimeout(() => controller.abort(), timeoutMs);
  try {
    const res = await fetch(url, {
      signal: controller.signal,
      headers: {
        // A lot of feeds behave better with a UA.
        'user-agent': 'OpenClaw ai-daily-digest fetcher (+https://openclaw.ai)'
      }
    });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    return await res.text();
  } finally {
    clearTimeout(id);
  }
}

async function mapLimit<T, R>(arr: T[], limit: number, fn: (item: T, i: number) => Promise<R>): Promise<R[]> {
  const out: R[] = new Array(arr.length);
  let idx = 0;

  async function worker() {
    while (true) {
      const i = idx++;
      if (i >= arr.length) break;
      out[i] = await fn(arr[i], i);
    }
  }

  const workers = Array.from({ length: Math.min(limit, arr.length) }, () => worker());
  await Promise.all(workers);
  return out;
}

function truncate(s: string, n: number) {
  if (!s) return '';
  const t = s.replace(/\s+/g, ' ').trim();
  return t.length <= n ? t : t.slice(0, n - 1) + '…';
}

function parseArgs(argv: string[]) {
  const args: Record<string, string> = {};
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a.startsWith('--')) {
      const k = a.slice(2);
      const v = argv[i + 1] && !argv[i + 1].startsWith('--') ? argv[++i] : 'true';
      args[k] = v;
    }
  }
  return args;
}

async function main() {
  const args = parseArgs(process.argv);
  const hours = Number(args.hours ?? '24');
  const outPath = args.output ?? '';
  const maxTotal = Number(args.maxTotal ?? '180');
  const perFeedCap = Number(args.perFeedCap ?? '3');

  if (!Number.isFinite(hours) || hours <= 0) {
    console.error('Invalid --hours');
    process.exit(1);
  }

  const since = Date.now() - hours * 3600 * 1000;

  const failedFeeds: OutJson['failedFeeds'] = [];

  const feedResults = await mapLimit(RSS_FEEDS, FEED_CONCURRENCY, async (feed) => {
    try {
      const xml = await fetchWithTimeout(feed.xmlUrl, FEED_FETCH_TIMEOUT_MS);
      const rawItems = parseRSSItems(xml);

      const articles: OutArticle[] = [];
      for (const item of rawItems) {
        const d = parseDate(item.pubDate);
        if (!d) continue;
        if (d.getTime() < since) continue;
        articles.push({
          title: truncate(item.title, 160),
          link: item.link,
          publishedAt: d.toISOString(),
          description: truncate(item.description, 320),
          sourceName: feed.name,
          sourceUrl: feed.htmlUrl,
        });
      }

      // keep most recent per feed
      articles.sort((a, b) => b.publishedAt.localeCompare(a.publishedAt));
      return articles.slice(0, perFeedCap);
    } catch (e: any) {
      failedFeeds.push({ name: feed.name, xmlUrl: feed.xmlUrl, error: String(e?.message ?? e) });
      return [];
    }
  });

  const flat = feedResults.flat();
  flat.sort((a, b) => b.publishedAt.localeCompare(a.publishedAt));
  const articles = flat.slice(0, maxTotal);

  const out: OutJson = {
    generatedAt: new Date().toISOString(),
    hours,
    totalFeeds: RSS_FEEDS.length,
    okFeeds: RSS_FEEDS.length - failedFeeds.length,
    failedFeeds,
    totalArticles: articles.length,
    articles,
  };

  const json = JSON.stringify(out, null, 2);

  if (outPath) {
    await mkdir(dirname(outPath), { recursive: true });
    await writeFile(outPath, json, 'utf8');
  } else {
    process.stdout.write(json + '\n');
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
